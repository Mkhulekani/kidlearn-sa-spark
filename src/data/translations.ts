export type LanguageCode = keyof typeof translations;

export const getTranslation = (language: LanguageCode, key: string): string => {
  const keys = key.split('.');
  let value: any = translations[language];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
};
